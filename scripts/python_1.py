"""1. Есть три целочисленные переменные,  нужно посчитать"""
a, b, c = 1, 5, 8
print(a + b + c) # Подсчитываем сумму
print(a - b - c) # Подсчитываем разность
print(a * b * c) # Подсчитываем произведение
print(a - b + c) # От первой переменной отнять вторую и прибавить третью
print((a * b) // c) # Поделить произведение двух переменных на третью
print((a + b) % c) #  От суммы первых двух переменных найти остаток от деления на третью переменную